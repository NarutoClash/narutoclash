import { BuildType } from '@/lib/battle-system/build-detector';

export type SummonBuildAbility = {
  name: string;
  description: string;
  emoji: string;
  effectType:
    | 'stat_bonus'
    | 'battle_passive'
    | 'turn_effect'
    | 'aura';
  value?: number;
  targetStat?: 'taijutsu' | 'ninjutsu' | 'genjutsu' | 'selo' | 'vitalidade' | 'inteligencia' | 'hp' | 'chakra';
};

export type Summon = {
  id: string;
  name: string;
  description: string;
  imageUrl: string;
  buffs: {
    taijutsu: number;
    ninjutsu: number;
    genjutsu: number;
    selo: number;
    vitalidade: number;
    inteligencia: number;
  };
  requiredLevel: number;
  price: number;
  trainableStats: string[];
  isPremium?: boolean;
  buildAbilities: Partial<Record<BuildType, SummonBuildAbility>>;
};

function ab(
  name: string, description: string, emoji: string,
  effectType: SummonBuildAbility['effectType'],
  value?: number, targetStat?: SummonBuildAbility['targetStat']
): SummonBuildAbility {
  return { name, description, emoji, effectType, value, targetStat };
}

