export function getSummonBuildAbility(summonId: string, build: BuildType): SummonBuildAbility | null {
  const summon = summonsData.find(s => s.id === summonId);
  if (!summon) return null;
  return summon.buildAbilities[build] ?? summon.buildAbilities['generico'] ?? null;
}

export function getSummonById(id: string): Summon | undefined {
  return summonsData.find(s => s.id === id);
}

export const getTrainingCost = (currentLevel: number): number => {
  if (currentLevel >= MAX_SUMMON_LEVEL) return 0;
  const BASE_TRAINING_COST = 10000;
  const COST_MULTIPLIER = 1.5;
  return Math.floor(BASE_TRAINING_COST * Math.pow(COST_MULTIPLIER, currentLevel - 1));
};

